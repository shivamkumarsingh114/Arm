 	
	
	AREA appcode, CODE, READONLY

	EXPORT __main
	ENTRY

__main FUNCTION
 
	MOV R5,#15;     TAKING FIRST NO. INTO REGISTER R5.
	MOV R6,#60;      TAKING SECOND ONE INTO REGISTER R6.
	MOV R7,#11;     TAKING THE THIRD NO. INTO REGISTER R7. 
	
	CMP R5,R6;      CALLING COMPARE INSTRUCTION TO MODIFY FLAGS.
	
	IT LT;          IF-THEN CONDITION BEING USED. 'LT' IS THE LESS THAN FLAG
	MOVLT R5,R6;    IF LT FLAG IS HIGH, THEN VALUE IN R5 TAKES R6'S VALUE.
	
	CMP R5,R7;      THE SAME THING NOW WITH REGISTER R5 AND R7 REGISTERS.
	
	IT LT;          SAME IF-THEN CONDITION.
    MOVLT R5,R7;    MOVING R7 VALUE INTO R5.
 
 
    ; R5 WILL CONTAIN THE LARGEST NO.
   
stop B stop; STOPS PROG.  
	ENDFUNC
	END	
	
	
	
	
	
	
	
	