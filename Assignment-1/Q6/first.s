 	
	
	AREA appcode, CODE, READONLY

	EXPORT __main
	ENTRY

__main FUNCTION
 
	 MOV R5,#20;  MOVE A VALUE TO R5. 
	 MOV R6,#8;  MOVE A VALUE TO R6.
	 CMP R5,R6 ;  COMPARE.
     BEQ OUTPUT ; IF EQUAL FLAG IS HIGH, JUMP TO OUTPUT DIRECTLY.
   

REDUCTION	  CMP R5,R6;     COMPARE.
			  ITE GT ;       IF R5>R6 THEN FLAG WOULD BE HIGH.
		      SUBGT R5,R5,R6;   IF HIGH, SUBTRACT R5 AND R6 VALUE AND STORE IN R5.
		      SUBLE R6,R6,R5;   ELSE DO THE OPPOSITE.
	          CMP R5,R6;        COMPARING TO DET THE FLAGS AGAIN.
	          BNE  REDUCTION     ;IF NOT EQUAL TO ZERO.  
OUTPUT         

 ; OUTPUT REFLECTS IN R5 REGISTER.

stop B stop; STOPS PROG.  
	ENDFUNC
	END	
	
	
	
	
	
	
	
	